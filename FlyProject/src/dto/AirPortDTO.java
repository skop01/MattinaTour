package dto;

public class AirPortDTO {
  String airname;
  String start_nation;
  String arrive_nation;
  String start_time;
  String arrive_time;
public String getAirname() {
	return airname;
}
public void setAirname(String airname) {
	this.airname = airname;
}

public String getStart_nation() {
	return start_nation;
}
public void setStart_nation(String start_nation) {
	this.start_nation = start_nation;
}
public String getArrive_nation() {
	return arrive_nation;
}
public void setArrive_nation(String arrive_nation) {
	this.arrive_nation = arrive_nation;
}
public String getStart_time() {
	return start_time;
}
public void setStart_time(String start_time) {
	this.start_time = start_time;
}
public String getArrive_time() {
	return arrive_time;
}
public void setArrive_time(String arrive_time) {
	this.arrive_time = arrive_time;
}
  
}
